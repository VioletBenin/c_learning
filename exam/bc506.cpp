/*---------------------------------------------
程序要求：
将字符串s中的数字字符放入d数组中
，然后输出d中的字符串。字符串s通过键盘输入。例如，输入的字符串为abc123edf345gh；执行程序后输出12345。
说明：输入的字符串可能含有空格。
输入：xhj34jkln456
输出：34456
----------------------------------------------*/
#include "stdio.h"

int main(int argc, char const *argv[]) {
  char s[255],d[255],*p=d;
  gets(s);
  for (int i = 0;*(s+i); i++) {
    if(*(s+i)>='0'&&*(s+i)<'9'){
      *p = *(s+i);
      p++;
    }
  }
  *p = '\0';
  puts(d);
  return 0;
}
