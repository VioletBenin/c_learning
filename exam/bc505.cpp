/*----------------------------------------------
3×3整型数组中，求“行号大于列号”元素之和。
说明：数组元素从键盘输入，数组元素间用空格分隔。
输入输出均为整型数据。
输入：1 2 3 4 2 8 0 1 2
输出：sum=5
----------------------------------------------*/
#include "stdio.h"
int main(int argc, char const *argv[]) {
  int i,j,sum=0,a[3][3];
  for (i = 0; i < 3; i++) {
    for ( j = 0;j < 3; j++) {
      scanf("%d", *(a+i)+j);
      if (i>j) {
        sum += *(*(a+i)+j);
      }
    }
  }
  printf("sum=%d\n", sum);
  return 0;
}
