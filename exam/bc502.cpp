/*--------------------------------------------------------
请编写程序，从键盘输入10个学生的成绩，统计及格（60分及其60分以上）的人数。
说明：成绩为整数，输入数据用空格分隔。
输入：90  54  88  72  45  60  65  78  97  90
输出：pass=8
----------------------------------------------------------*/
#include "stdio.h"
int main(int argc, char const *argv[]) {
  int a[10],pass=0;
  for (int i = 0; i < 10; i++) {
    scanf("%d", a+i);
    if(*(a+i)>59){
      pass++;
    }
  }
  printf("pass=%d\n", pass);
  return 0;
}
