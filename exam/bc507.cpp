/*---------------------------------------------
程序要求：
从键盘上输入一个字符串，放在数组中，
要求将字符串中的大写字母转换成小写字母，
小写字母转换成大写字母，非字母字符不变，并输出。

输入：ABC123def
输出：abc123DEF
----------------------------------------------*/
#include "stdio.h"
int main(int argc, char const *argv[]) {
  char a[255],*p;
  gets(a);
  for (p = a; *p; p++) {
    if (*p>='a'&&*p<='z') {
      *p -= 32;
    }
    else if (*p>='A'&&*p<='Z') {
      *p += 32;
    }
  }
  puts(a);
  return 0;
}
