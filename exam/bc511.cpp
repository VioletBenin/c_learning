/*---------------------------------------------
程序要求：
求4*4整型数组的主对角线元素的和。
说明：数组元素从键盘输入，数组元素间用空格分开。
输入输出均为整型。

输入：1 2 3 4 2 8 0 1 2 6 7 5 1 7 9 5
输出：21
----------------------------------------------*/
#include "stdio.h"
int main(int argc, char const *argv[]) {
  int a[4][4],sum=0,i,j;
  for (i = 0; i < 4; i++) {
    for ( j = 0; j < 4; j++) {
      scanf("%d", *(a+i)+j);
      if (i==j) {
        sum += *(*(a+i)+j);
      }
    }
  }
  printf("%d\n", sum);
  return 0;
}
