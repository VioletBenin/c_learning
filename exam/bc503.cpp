/*---------------------------------------------
编程要求：
请编写一个程序，求一堆数组中10个学生的平均成绩，
以实型数形式输出结果，保留两位小数。
输入：67 56 89 92 78 64 52 68 47 73
输出： aver=68.60
----------------------------------------------*/
#include "stdio.h"
int main(int argc, char const *argv[]) {
  int a[10];
  float s;
  for (int i = 0; i < 10; i++) {
    scanf("%d", a+i);
    s += *(a+i);
  }
  printf("aver=%.2f\n", s/10);
  return 0;
}
